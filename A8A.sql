-- MEMBERSHIP
CREATE TABLE MEMBERSHIP(
MEM_NUM		INTEGER		NOT NULL	UNIQUE,
MEM_FNAME	VARCHAR(20) NOT NULL,
MEM_LNAME	VARCHAR(25)	NOT NULL,
MEM_STREET	VARCHAR(35) NOT NULL,
MEM_CITY	VARCHAR(25)	NOT	NULL,
MEM_STATE	CHAR(2)		NOT	NULL,
MEM_ZIP		CHAR(5)		NOT	NULL,
MEM_BALANCE	NUMERIC(9,2)	DEFAULT 0.00,
PRIMARY KEY (MEM_NUM)
)

-- RENTAL
CREATE TABLE RENTAL(
RENT_NUM	INTEGER		NOT NULL	UNIQUE,
RENT_DATE	DATE		NOT NULL,
MEM_NUM		INTEGER,
PRIMARY KEY (RENT_NUM),
FOREIGN KEY (MEM_NUM) REFERENCES MEMBERSHIP ON UPDATE CASCADE
)

--PRICE
CREATE TABLE PRICE(
PRICE_CODE			INTEGER		NOT NULL	UNIQUE,
PRICE_DESCRIPTION	VARCHAR(35) NOT NULL,
PRICE_RENTFEE		NUMERIC(9,2) DEFAULT 0.00,
PRICE_DAILYLATEFEE  NUMERIC(9,2) DEFAULT 0.00,
PRIMARY KEY (PRICE_CODE)
)

--MOVIE
CREATE TABLE MOVIE(
MOVIE_NUM	INTEGER		NOT NULL	UNIQUE,
MOVIE_TITLE	VARCHAR(35)	NOT NULL,
MOVIE_YEAR	CHAR(4)		NOT NULL,
MOVIE_COST	NUMERIC(9,2) DEFAULT 0.00,
MOVIE_GENRE VARCHAR(15)	NOT NULL,
PRICE_CODE	INTEGER,
PRIMARY KEY (MOVIE_NUM),
FOREIGN KEY (PRICE_CODE) REFERENCES PRICE ON UPDATE CASCADE
)

--VIDEO
CREATE TABLE VIDEO(
VID_NUM		INTEGER		NOT NULL,
VID_INDATE	DATE		NOT NULL,
MOVIE_NUM	INTEGER,
PRIMARY KEY (VID_NUM),
FOREIGN KEY (MOVIE_NUM) REFERENCES MOVIE ON UPDATE CASCADE
)

-- DETAILRENTAL
CREATE TABLE DETAILRENTAL(
RENT_NUM			INTEGER		NOT NULL, 
VID_NUM				INTEGER		NOT NULL,
DETAIL_FEE			NUMERIC(9,2) DEFAULT 0.00,
DETAIL_DUEDATE		DATETIME	NOT NULL,
DETAIL_RETURNDATE	DATETIME,
DETAIL_DAILYLATEFEE NUMERIC(9,2),
PRIMARY KEY (RENT_NUM, VID_NUM),
FOREIGN KEY (RENT_NUM) REFERENCES RENTAL ON UPDATE CASCADE,
FOREIGN KEY	(VID_NUM) REFERENCES VIDEO ON UPDATE CASCADE
)

